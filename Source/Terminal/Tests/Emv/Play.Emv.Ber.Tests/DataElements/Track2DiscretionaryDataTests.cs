        byte[] expectedResult = testData.EncodeTagLengthValue();
        byte[]? testValue = sut.EncodeTagLengthValue();

        Assert.Equal(testValue, expectedResult);
    }

    /// <summary>
    ///     BerEncoding_EncodingToTagLengthValue_SerializesExpectedValue
    /// </summary>
    /// <exception cref="InvalidOperationException"></exception>
    /// <exception cref="BerParsingException"></exception>
    [Fact]
    public void BerEncoding_EncodingToTagLengthValue_SerializesExpectedValue()
    {
        Track2DiscretionaryDataTestTlv testData = new();
        Track2DiscretionaryData sut = Track2DiscretionaryData.Decode(testData.EncodeValue().AsSpan());
        TagLengthValue? testValue = sut.AsTagLengthValue();
        TagLengthValue expectedResult = new(Track2DiscretionaryData.Tag, testData.EncodeValue());
        Assert.Equal(testValue, expectedResult);
    }

    /// <summary>
    ///     TagLengthValue_SerializingToBer_ReturnsExpectedResult
    /// </summary>
    /// <exception cref="InvalidOperationException"></exception>
    /// <exception cref="BerParsingException"></exception>
    [Fact]
    public void TagLengthValue_SerializingToBer_ReturnsExpectedResult()
    {
        Track2DiscretionaryDataTestTlv testData = new();
        Track2DiscretionaryData sut = Track2DiscretionaryData.Decode(testData.EncodeValue().AsSpan());

        byte[] testValue = sut.AsTagLengthValue().EncodeTagLengthValue();
        byte[] expectedResult = testData.EncodeTagLengthValue();
        Assert.Equal(testValue, expectedResult);
    }

    /// <summary>
    ///     InvalidBerEncoding_DeserializingDataElement_Throws
    /// </summary>
    /// <exception cref="InvalidOperationException"></exception>
    /// <exception cref="DataElementParsingException"></exception>
    [Fact]
    public void InvalidBerEncoding_DeserializingData_Throws()
    {
        Track2DiscretionaryDataTestTlv testData = new(new byte[]
        {
            61, 84, 98, 62, 33, 44, 92, 42, 80, 64,
            62, 83, 99, 61, 34, 44, 92, 43, 80, 65
        });

        Assert.Throws<DataElementParsingException>(() => Track2DiscretionaryData.Decode(testData.EncodeValue().AsSpan()));
    }


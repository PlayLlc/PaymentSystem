    public void BerEncoding_EncodingDataElement_SerializesExpectedValue()
    {
        ProtectedDataEnvelope3TestTlv testData = new();
        ProtectedDataEnvelope3 sut = ProtectedDataEnvelope3.Decode(testData.EncodeValue().AsSpan());
